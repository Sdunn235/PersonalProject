# protocols.py — Protocol interfaces for the data layer
# Mirrors RPGDatabaseManager's IContext + IEntityDao<T>
from __future__ import annotations
from typing import Protocol, TypeVar, Callable, runtime_checkable

T = TypeVar("T")


@runtime_checkable
class IEntityDao(Protocol[T]):
    """Generic DAO interface — Python equivalent of IEntityDao<T>."""

    def get_all(self) -> list[T]: ...
    def get_by_id(self, record_id: str) -> T | None: ...
    def where(self, predicate: Callable[[T], bool]) -> list[T]: ...
    def first_or_default(self, predicate: Callable[[T], bool]) -> T | None: ...
    def any(self, predicate: Callable[[T], bool]) -> bool: ...
    def count(self, predicate: Callable[[T], bool] | None = None) -> int: ...
    def add(self, record: T) -> None: ...
    def update(self, record_id: str, updates: dict) -> bool: ...
    def delete(self, record_id: str) -> bool: ...


@runtime_checkable
class IContext(Protocol):
    """Single owner of all DAOs — Python equivalent of IContext."""

    @property
    def entities(self) -> IEntityDao: ...

    @property
    def abilities(self) -> IEntityDao: ...

    @property
    def items(self) -> IEntityDao: ...

    def reload(self) -> None: ...
    def save(self) -> None: ...
